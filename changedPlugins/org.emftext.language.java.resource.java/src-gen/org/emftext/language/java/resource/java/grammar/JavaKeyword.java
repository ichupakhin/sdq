package org.emftext.language.java.resource.java.grammar;


/**
 * A class to represent a keyword in the grammar.
 */
public class JavaKeyword extends org.emftext.language.java.resource.java.grammar.JavaSyntaxElement {
	
	private final String value;
	
	public JavaKeyword(String value, org.emftext.language.java.resource.java.grammar.JavaCardinality cardinality) {
		super(cardinality, null);
		this.value = value;
	}
	
	public String getValue() {
		return value;
	}
	
	public String toString() {
		return "\"" + value + "\"";
	}
	
}
