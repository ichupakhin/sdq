package org.emftext.language.java.resource.java.grammar;

import org.eclipse.emf.ecore.EStructuralFeature;

public class JavaTerminal extends org.emftext.language.java.resource.java.grammar.JavaSyntaxElement {
	
	private final EStructuralFeature feature;
	private final int mandatoryOccurencesAfter;
	
	public JavaTerminal(EStructuralFeature feature, org.emftext.language.java.resource.java.grammar.JavaCardinality cardinality, int mandatoryOccurencesAfter) {
		super(cardinality, null);
		this.feature = feature;
		this.mandatoryOccurencesAfter = mandatoryOccurencesAfter;
	}
	
	public EStructuralFeature getFeature() {
		return feature;
	}
	
	public int getMandatoryOccurencesAfter() {
		return mandatoryOccurencesAfter;
	}
	
	public String toString() {
		return feature.getName() + "[]";
	}
	
}
